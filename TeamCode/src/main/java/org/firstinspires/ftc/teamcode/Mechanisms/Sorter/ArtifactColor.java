package org.firstinspires.ftc.teamcode.Mechanisms.Sorter;

/**
 * The two artifact colors used by the FTC DECODE game.
 *
 * <p>This enum intentionally excludes an UNKNOWN value. Unknown or partially known slot state is
 * represented by {@link SlotContent}, because "unknown" is a property of the full slot belief,
 * not a real artifact color.
 */
public enum ArtifactColor {
    /** Purple artifact. */
    PURPLE,

    /** Green artifact. */
    GREEN
}
